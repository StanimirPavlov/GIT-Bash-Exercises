using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Sequence_2kPlus1
{
    class Program
    {
        static void Main(string[] args)
        {
            var n = int.Parse(Console.ReadLine());
            var one = 1;

            while (one <= n)
            {
                Console.WriteLine(one);
                one = (2 * one) + 1;
            }
        }
    }
}
